# Mutable (can be changed)	
# Syntax	[] (square brackets)	
# Performance	Slightly slower	
# Methods	Many built-in methods (like .append())	
# Use Case	Used when data may need to change	
# Example	my_list = [1, 2, 3]

tea=["ice tea","green tea","black tea"]
print(tea[0])

for i in tea:
    print(i,end="-")
    print(i)

for i in range(1,5):
    print(i)